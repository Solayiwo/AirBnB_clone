#!/usr/bin/python3
"""
BaseModel module
Defines the BaseModel class which serves as the base class for other models.
"""

import uuid
from datetime import datetime
import models


class BaseModel:
    """
    Base class for all models, defining
    common attributes and methods.
    """

    def __init__(self, *args, **kwargs):
        """
        Initializes a new instance of BaseModel. If kwargs is not empty,
        each key of this dictionary is an attribute name.
        Otherwise, create id and created_at attributes as new instance.
        """
        if kwargs:
            for key, value in kwargs.items():
                if key == "created_at" or key == "updated_at":
                    value = datetime.fromisoformat(value)
                if key != "__class__":
                    setattr(self, key, value)
        else:
            self.id = str(uuid.uuid4())
            self.created_at = datetime.now()
            self.updated_at = self.created_at
            models.storage.new(self)

    def __str__(self):
        """Returns a string representation of the instance."""
        return f"[{self.__class__.__name__}] ({self.id}) {self.__dict__}"

    def save(self):
        """
        Updates the public instance attribute updated_at
        with the current datetime
        """
        self.updated_at = datetime.now()
        models.storage.save()

    def to_dict(self):
        """
        Returns a dictionary containing all keys/values
        of __dict__ of the instance
        """
        dict_repr = self.__dict__.copy()
        dict_repr['__class__'] = self.__class__.__name__
        dict_repr['created_at'] = self.created_at.isoformat()
        dict_repr['updated_at'] = self.updated_at.isoformat()
        return dict_repr
